#include <unistd.h>

void	ft_putnbr(int nb)
{
	int min;

	min = 0;
	if (nb < 0)
	{
		write('-');
		if (nb == -2147483648)
		{
			min = 1;
			nb = 2147483647;
		}
		else
			nb = nb * -1;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	write((nb % 10 + min) + '0');
}
